import math

# Вариант 52.
n = 19 # Число источников.
requests_per_month = 3  # Заявок в месяц (k из условия).
t_service_days = 1.5  # Время обслуживания (одной заявки).
P_required = 90 # Требуемый процент работающих.


lam = requests_per_month / (30 * n) # Интенсивность от одного источника.
mu = 1.0 / t_service_days # Интенсивность обслуживания
rho = lam / mu # ПРриведённая интенсивность

# 1. Вычисляем p0 по формуле Энгсета.
# state - число неисправных источников.
denom = 0.0
for state in range(n + 1):
    comb = math.factorial(n) / math.factorial(n - state)
    denom += comb * (rho ** state)
p0 = 1.0 / denom

# 2. Нам нужно p1 (одна неисправная машина).
p1 = n * rho * p0   # то же самое: n!/(n-1)! * rho^1 * p0.

# 3. Вероятность, что активны не менее 90% (неисправных <= 1).
prob_active = p0 + p1

# 4. Характеристики.
A = mu * (1 - p0) # Абсолютная пропускная способность
L_sys = n - A / lam # Среднее число неисправных.
L_queue = L_sys - (1 - p0) # Среднее число в очереди.
T_sys = L_sys / A # Среднее время пребывания.
T_queue = L_queue / A # Среднее время ожидания.

print(f"n = {n}, заявок в месяц = {requests_per_month}, t = {t_service_days} дней, P = {P_required}%")
print(f"λ = {lam:.6f}, μ = {mu:.4f}, ρ = {rho:.6f}\n")

print("1. Вероятность, что активны не менее 90% источников:")
print(f"   P = {prob_active:.6f} ({prob_active*100:.4f}%)\n")

print("2. Характеристики:")
print(f"   p0 = {p0:.6f}")
print(f"   A = {A:.6f} заявок/день (абсолютно пропускная способность)")
print(f"   L_сист = {L_sys:.6f} (неисправных источников)")
print(f"   T_сист = {T_sys:.6f} дней (время ремонта + ожидания)")
print(f"   T_оч = {T_queue:.6f} дней (время ожидания ремонта)")